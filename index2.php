							<div class="col-md-12">
							<p>Passionated by electronic alternative music since 2000, PINGU created Bankizz Label 15 years ago, in 2004, organizing alternative electronic raves, first clandestine ones but quickly more legals to be able to book local or foreign emergent talents. He contributed like that to enrich the Belgian and international scene by inviting some actual headliners for the first time in Belgium, for example ; “Katharsys, The Satan, eDUB, Micromakine, etc.” and other confirmed artists like “Detest, Sei2ure, The Outside Agency, The DJ Producer, Igneon System, Somatic Responses, etc.” (The list is quite long…).</p>
							<p>Lots of local liver’s and deejay’s have been invited among them, playing through more than 200 BKZ events, mostly in Belgium (Brussels, Antwerp, Liège, Leuven, Charleroi, etc.), but also in France, Bulgaria, Switzerland. Stéphane aka Pingu is the manager of Bankizz Records, a Free Net Label that released over 40 albums by artists from all over the world. Nowadays, he’s more involved into playing music than before, hosting radio shows, drawing, etc. and less invested himself in organizations.</p>
							<p>PINGU BKZ plays hard on CDJ’s turntables some industrial and crossbreed hardcore, breaked as fuck!</p>
